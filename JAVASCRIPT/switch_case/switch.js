/* Switch - Case */

var parametro = parseInt(prompt("Digit um número"))

switch (parametro) {
//O switch usa o comparador idêntico ===
case 1:
	document.write("parametro 1")
	break

case 2:
	document.write('parametro 2')
	break

default:
	document.write("Default")
	//se parametro não for encontrado

}